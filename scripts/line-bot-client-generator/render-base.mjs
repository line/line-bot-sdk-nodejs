import { OUTPUT_NAMES } from "./constants.mjs";
import {
  indentBlock,
  normalizeComment,
  uniqueClientPackages,
} from "./text.mjs";

export function renderBaseFile(clients) {
  const uniquePackages = uniqueClientPackages(clients);

  const usesReadable = clients.some(client =>
    client.methods.some(
      method =>
        method.parameterList.includes("Readable") ||
        method.returnType.includes("Readable"),
    ),
  );

  const usesSdkTypes = clients.some(client =>
    client.methods.some(
      method =>
        method.parameterList.includes("Types.") ||
        method.returnType.includes("Types."),
    ),
  );

  const sections = [];
  sections.push(`/**
* This file is autogenerated.
*
* Generated by scripts/generate-line-bot-client.mjs.
* Do not edit this file directly.
*/`);

  if (usesReadable) {
    sections.push(`import type { Readable } from "node:stream";`);
  }

  if (usesSdkTypes) {
    sections.push(`import type * as Types from "./types.js";`);
  }

  sections.push(
    ...uniquePackages.map(
      ({ packageDir, namespaceAlias }) =>
        `import type * as ${namespaceAlias} from "./${packageDir}/api.js";`,
    ),
  );

  sections.push(
    [
      `export interface ${OUTPUT_NAMES.delegatesTypeName} {`,
      ...clients.map(
        client =>
          `  readonly ${client.delegateName}: ${client.namespaceAlias}.${client.className};`,
      ),
      `}`,
    ].join("\n"),
  );

  const classLines = [
    `export abstract class ${OUTPUT_NAMES.baseClassName} {`,
    `  protected abstract readonly clients: ${OUTPUT_NAMES.delegatesTypeName};`,
    "",
  ];

  const renderedMethods = [];
  for (const client of clients) {
    for (const method of client.methods) {
      const methodLines = [];

      if (method.comment) {
        methodLines.push(indentBlock(normalizeComment(method.comment)));
      }

      methodLines.push(
        `  public ${method.asyncKeyword}${method.methodName}${method.typeParameters}(${method.parameterList}): ${method.returnType} {`,
      );
      methodLines.push(
        `    return this.clients.${method.delegateName}.${method.methodName}(${method.argumentList});`,
      );
      methodLines.push("  }");

      renderedMethods.push(methodLines.join("\n"));
    }
  }

  classLines.push(renderedMethods.join("\n\n"));
  classLines.push("}");

  sections.push(classLines.join("\n"));
  return `${sections.filter(Boolean).join("\n\n")}\n`;
}
